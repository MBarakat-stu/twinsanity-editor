        public static string ToName(SectionType section, uint ID)
        {
            Dictionary<uint, string> targetDict;
            switch (section)
            {
                default:
                    return ID.ToString("X8");
                case SectionType.Skydome:
                    targetDict = Hash_SkyDomes;
                    break;
                case SectionType.Object:
                case SectionType.ObjectDemo:
                    return ((DefaultEnums.ObjectID)ID).ToString();
                case SectionType.Script:
                case SectionType.ScriptDemo:
                case SectionType.ScriptX:
                    return ((DefaultEnums.ScriptID)ID).ToString();
                case SectionType.BlendSkin:
                case SectionType.BlendSkinX:
                    targetDict = Hash_BlendSkins;
                    break;
                case SectionType.CollisionSurface:
                    return ((DefaultEnums.SurfaceTypes)ID).ToString();
                case SectionType.InstanceTemplate:
                case SectionType.InstanceTemplateDemo:
                    targetDict = Hash_Templates;
                    break;
                case SectionType.OGI:
                    targetDict = Hash_OGI;
                    break;
                case SectionType.Skin:
                case SectionType.SkinX:
                    targetDict = Hash_Skins;
                    break;
                case SectionType.SE:
                case SectionType.SE_Eng:
                case SectionType.SE_Fre:
                case SectionType.SE_Ger:
                case SectionType.SE_Ita:
                case SectionType.SE_Jpn:
                case SectionType.SE_Spa:
                case SectionType.Xbox_SE:
                case SectionType.Xbox_SE_Eng:
                case SectionType.Xbox_SE_Fre:
                case SectionType.Xbox_SE_Ger:
                case SectionType.Xbox_SE_Ita:
                case SectionType.Xbox_SE_Jpn:
                case SectionType.Xbox_SE_Spa:
                    targetDict = Hash_Sounds;
                    break;
                case SectionType.CodeModel:
                case SectionType.CodeModelDemo:
                case SectionType.CodeModelX:
                    targetDict = Hash_CustomAgents;
                    break;
                case SectionType.Texture:
                case SectionType.TextureX:
                    targetDict = Hash_Textures;
                    break;
                case SectionType.Animation:
                    targetDict = Hash_Animations;
                    break;
            }

            if (targetDict.ContainsKey(ID))
                return targetDict[ID];
            else
                return ID.ToString("X8");
        }
        public static string SkyToName(Skydome sky)
        {
            return Hash_SkyDomes[sky.ID];
        }
        public static string SkyToName(uint ID)
        {
            return Hash_SkyDomes[ID];
        }

        public static string TexToName(uint ID, uint Hash)
        {
            if (Hash_Textures.ContainsKey(ID))
                return Hash_Textures[ID];
            else if (Hash_DupeTextures.ContainsKey((ID, Hash)))
                return Hash_DupeTextures[(ID, Hash)];
            else
                return $"{ID:X8}_{Hash:X8}";
        }

